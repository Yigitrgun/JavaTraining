public class Main {
    public static void main(String[] args) {
        //for
        for (int i = 1; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println(" For döngüsü Bitti");

        //While
        int i = 2;
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        //Do-While
        int j = 1;
        do {
            System.out.println(j);
            j += 2;
        } while (j < 20);


    }
}